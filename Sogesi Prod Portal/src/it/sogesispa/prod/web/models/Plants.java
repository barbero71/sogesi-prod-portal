package it.sogesispa.prod.web.models;

public class Plants
{

	private int stabId;
	private int tiNumber;
	private String stabName;
	private String stabCdc;

	public int getStabId()
	{
		return stabId;
	}

	public void setStabId(int stabId)
	{
		this.stabId = stabId;
	}

	public int getTiNumber()
	{
		return tiNumber;
	}

	public void setTiNumber(int tiNumber)
	{
		this.tiNumber = tiNumber;
	}

	public String getStabName()
	{
		return stabName;
	}

	public void setStabName(String stabName)
	{
		this.stabName = stabName;
	}

	public String getStabCdc()
	{
		return stabCdc;
	}

	public void setStabCdc(String stabCdc)
	{
		this.stabCdc = stabCdc;
	}

	
	
}
